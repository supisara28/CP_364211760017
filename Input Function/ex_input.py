# Input Function

# input()

# input str
print("What's your name: ")
name = input()
print(f'Hello, {name}')

# cast str to int
age = int(input('How old are you: '))
print(f'{name} is {age} years old.')

# cast str to float
weight = float(input('How much your weight: '))
print(f'your, weight is: {weight}')

print(name, type(name))
print(age, type(age))
print(weight, type(weight))

